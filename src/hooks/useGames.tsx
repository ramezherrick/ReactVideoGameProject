const useGames = (
  selectedGenre: Genres | null,
  selectedPlatform: Platform | null
) =>
  useGenericHook<Game>(
    "/games",
    { params: { genres: selectedGenre?.id, platforms: selectedPlatform?.id } },
    [selectedGenre?.id, selectedPlatform?.id]
  );
